// Function to Find the Area of Triangle
function areaRectangle(length, width) {
  let area = length * width;
  return area;
}

// Function to Find the Parameter of Triangle
function perimeterRectangle(length, width) {
let perimeter = 2 * (length + width);
return perimeter;
}

//input
let length = 10;
let width = 20;

console.log("Area = " + areaRectangle(length, width));
console.log("Perimeter = " + perimeterRectangle(length, width)); 



